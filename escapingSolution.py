# By using '\n' can line feed
# By using '\t' can make tab(4 blankspace)
# By using '\r' can enter(overwrite last word)
# By using '\b' can back one space

print('hello\nworld')
print('hello\tworld')
print('hello\rworld')
print('hello\bworld')

# By using '\\' can output only one '\'
# By using '\'' can print '
# By using 'r'or'R' behind character string can 
#  make escaping character invalidation

print('https:\\\\www.bing.com')
print('He said:\'Hello,everyone\'')
print(r'hello\nworld')